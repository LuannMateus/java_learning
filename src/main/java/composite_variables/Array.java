package composite_variables;

public class Array {
  public static void main(String[] args) {
    /*
     * Declaração de arrays.
     */
    String[] names;
    String lastNames[];

    /*
     * Declaração e atribuição de arrays.
     */
    String[] superHeroes = new String[2];
    String villains[] = new String[2];

    /*
     * Atribuição de valores.
     */
    superHeroes[0] = "Superman";
    villains[0] = "Lex Lutor";

    /*
     * Declaração e atribuição de valores.
     */
    int grade[] = { 10, 9, 8 };
    int[] revenues = new int[] { 10, 20, 11 };

    /*
     * Exemplos com outros tipos primitivos
     */
    byte bytes[] = new byte[4];
    short shots[] = new short[8];
    double doubles[] = new double[7];
    float floats[] = new float[3];
    String strings[] = new String[10];

  }
}
